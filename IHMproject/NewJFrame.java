    public void tabledata()
    {
      int r = jTable1.getSelectedRow() ;
      
      String name = jTable1.getValueAt (r, 0) .toString () ;
      String address = jTable1.getValueAt (r, 1) .toString () ;
      String age = jTable1.getValueAt (r, 2) .toString () ;
      String telephone = jTable1.getValueAt (r,3) .toString () ;
      String ID = jTable1.getValueAt (r,4) .toString () ;
      
      
          namebox. setText(name);
          addressbox.setText(address);
          agebox.setText(age);
          telbox. setText(telephone);
          idbox.setText(ID);
    }
    
    public void update (){
        
      String name = namebox.getText () ;
      String address = addressbox.getText () ;
      int age = Integer.parseInt (agebox.getText ()) ;
      int telephone = Integer.parseInt (telbox.getText () ) ;
      int id = Integer.parseInt (idbox.getText () ) ;
       try {

         String sql = "UPDATE tablenew SET tname = '"+name+"',taddress = '"+address+"',tage = '"+age+"',ttel = '"+telephone+"' WHERE ID ='"+id+"' ";
         sss = sos.prepareStatement (sql);
         sss.execute () ;
         
         JOptionPane. showMessageDialog(null, "Updated");
         
       } catch (Exception e) {
           
          JOptionPane. showMessageDialog (null, "Error");
      
    }
       table();
    }
    
 

    
